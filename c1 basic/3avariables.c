#include <stdio.h>

int main()
{
    int a = 1;

    float b = 1.4;

    char c;   
    c = 'a';   //NOTE: Here character ko apko single quote ma hi rakhna hai na ki double quotes ma.

    printf("The value of a is %d\n", a);     // %d represents int. number.
    printf("The value of b is %f\n", b);     // %f is used to represent the floating number.
    printf("The value of c is %c\n", c);     // %c is used to represent the character.
}