// These allow the program to make decisions and execute certain parts of code based on conditions.
// a) if Statement
// b) if-else Statement
// c) else if Ladder
// d) switch Statement

// 3. Loop Control instructions (Iteration) :-  (Will be studied later)
// These repeat a block of code multiple times based on a condition.
// a) while Loop
// b) do-while Loop
// c) for Loop

//  4. Jump Control Instructions:-    (will be studied later)
// These directly change the flow of control.
