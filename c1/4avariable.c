#include <stdio.h>

int main() {
    int age;
    printf("Enter your age: ");
    scanf("%d", &age);       //%d here is also used to represent the integer value in age varable and  &age is used to take the value from the user.
    printf("Your age is %d\n", age);
    printf("best of luck");
    return 0;
}

// In order to take input from the user and assign it to a variable, we use scanf() function






